    <script>
        // Configuration
        const API_BASE_URL = 'auth.php';

        // DOM Elements
        const tabs = document.querySelectorAll('.tab');
        const forms = document.querySelectorAll('.form-container');
        const alertDiv = document.getElementById('alert');

        // Initialize
        document.addEventListener('DOMContentLoaded', function() {
            setupEventListeners();
            document.getElementById('loginUsername').focus();
        });

        function setupEventListeners() {
            // Tab switching
            tabs.forEach(tab => {
                tab.addEventListener('click', function() {
                    const targetTab = this.dataset.tab;
                    
                    // Update tabs
                    tabs.forEach(t => t.classList.remove('active'));
                    this.classList.add('active');
                    
                    // Update forms
                    forms.forEach(form => form.classList.remove('active'));
                    document.getElementById(targetTab + 'Form').classList.add('active');
                    
                    // Clear alerts
                    clearAlert();
                });
            });

            // Password toggles
            document.querySelectorAll('.password-toggle').forEach(toggle => {
                toggle.addEventListener('click', function() {
                    const targetId = this.dataset.target;
                    const input = document.getElementById(targetId);
                    const type = input.getAttribute('type') === 'password' ? 'text' : 'password';
                    input.setAttribute('type', type);
                    this.innerHTML = type === 'password' ? '<i class="fas fa-eye"></i>' : '<i class="fas fa-eye-slash"></i>';
                });
            });

            // Password strength checking
            document.getElementById('regPassword').addEventListener('input', function() {
                checkPasswordStrength(this.value);
            });

            // Form submissions
            document.getElementById('loginFormElement').addEventListener('submit', handleLogin);
            document.getElementById('registerFormElement').addEventListener('submit', handleRegister);
        }

        function checkPasswordStrength(password) {
            const requirements = {
                length: password.length >= 8,
                upper: /[A-Z]/.test(password),
                lower: /[a-z]/.test(password),
                number: /[0-9]/.test(password),
                special: /[^A-Za-z0-9]/.test(password)
            };

            // Update requirement indicators
            Object.keys(requirements).forEach(req => {
                const element = document.getElementById('req' + req.charAt(0).toUpperCase() + req.slice(1));
                if (requirements[req]) {
                    element.classList.add('met');
                    element.classList.remove('unmet');
                    element.innerHTML = '<i class="fas fa-check"></i> ' + element.textContent.replace('●', '').trim();
                } else {
                    element.classList.add('unmet');
                    element.classList.remove('met');
                    element.innerHTML = '<i class="fas fa-circle"></i> ' + element.textContent.replace('✓', '').trim();
                }
            });

            // Calculate strength score
            const metCount = Object.values(requirements).filter(Boolean).length;
            const totalCount = Object.keys(requirements).length;
            const strength = (metCount / totalCount) * 100;

            updateStrengthIndicator(strength);
        }

        function updateStrengthIndicator(strength) {
            const strengthBar = document.getElementById('strengthBar');
            const strengthText = document.getElementById('strengthText');
            
            strengthBar.className = 'strength-bar';
            strengthBar.style.width = strength + '%';
            
            if (strength === 0) {
                strengthText.textContent = 'Password strength';
                strengthText.style.color = 'rgba(255, 255, 255, 0.7)';
            } else if (strength < 40) {
                strengthBar.classList.add('strength-weak');
                strengthText.textContent = 'Weak password';
                strengthText.style.color = '#ff4757';
            } else if (strength < 70) {
                strengthBar.classList.add('strength-fair');
                strengthText.textContent = 'Fair password';
                strengthText.style.color = '#ffa502';
            } else if (strength < 90) {
                strengthBar.classList.add('strength-good');
                strengthText.textContent = 'Good password';
                strengthText.style.color = '#2ed573';
            } else {
                strengthBar.classList.add('strength-strong');
                strengthText.textContent = 'Strong password';
                strengthText.style.color = '#1DB954';
            }
        }

        async function handleLogin(e) {
            e.preventDefault();
            
            const username = document.getElementById('loginUsername').value.trim();
            const password = document.getElementById('loginPassword').value.trim();
            const loginBtn = document.getElementById('loginBtn');

            if (!username || !password) {
                showAlert('Please fill in all fields', 'error');
                return;
            }

            loginBtn.disabled = true;
            loginBtn.innerHTML = '<i class="fas fa-spinner loading-spinner"></i> Signing in...';

            try {
                const response = await fetch(API_BASE_URL, {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify({
                        action: 'login',
                        username: username,
                        password: password
                    })
                });

                const data = await response.json();

                if (data.success) {
                    handleSuccessfulLogin(data);
                } else {
                    handleFailedLogin(data.message, 'loginFormElement');
                }
            } catch (error) {
                handleFailedLogin('Network error. Please try again.', 'loginFormElement');
            } finally {
                loginBtn.disabled = false;
                loginBtn.innerHTML = '<i class="fas fa-sign-in-alt"></i> Login to Musicfy';
            }
        }

        async function handleRegister(e) {
            e.preventDefault();
            
            const fullName = document.getElementById('fullName').value.trim();
            const username = document.getElementById('regUsername').value.trim();
            const email = document.getElementById('regEmail').value.trim();
            const password = document.getElementById('regPassword').value.trim();
            const registerBtn = document.getElementById('registerBtn');

            if (!username || !email || !password) {
                showAlert('Please fill in all required fields', 'error');
                return;
            }

            if (password.length < 8) {
                showAlert('Password must be at least 8 characters long', 'error');
                return;
            }

            registerBtn.disabled = true;
            registerBtn.innerHTML = '<i class="fas fa-spinner loading-spinner"></i> Creating account...';

            try {
                const response = await fetch(API_BASE_URL, {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify({
                        action: 'register',
                        username: username,
                        email: email,
                        password: password,
                        full_name: fullName
                    })
                });

                const data = await response.json();

                if (data.success) {
                    showAlert(data.message, 'success');
                    // Switch to login tab after successful registration
                    setTimeout(() => {
                        document.querySelector('[data-tab="login"]').click();
                        document.getElementById('loginUsername').value = username;
                        document.getElementById('loginPassword').value = '';
                    }, 2000);
                } else {
                    handleFailedLogin(data.message, 'registerFormElement');
                }
            } catch (error) {
                handleFailedLogin('Network error. Please try again.', 'registerFormElement');
            } finally {
                registerBtn.disabled = false;
                registerBtn.innerHTML = '<i class="fas fa-user-plus"></i> Create Account';
            }
        }

        function handleSuccessfulLogin(data) {
            showAlert('Login successful! Welcome to Musicfy.', 'success');
            
            // Store session data
            localStorage.setItem('musicfy_session', data.session_token);
            localStorage.setItem('musicfy_user', JSON.stringify(data.user));
            localStorage.setItem('musicfy_last_login', new Date().toISOString());
            
            // Redirect to dashboard
            setTimeout(() => {
                window.location.href = 'dashboard.php';
            }, 1500);
        }

        function handleFailedLogin(message, formId) {
            showAlert(message, 'error');
            shakeForm(formId);
        }

        function showAlert(message, type) {
            alertDiv.textContent = message;
            alertDiv.className = `alert alert-${type}`;
            alertDiv.style.display = 'block';
            
            setTimeout(() => {
                alertDiv.style.display = 'none';
            }, 5000);
        }

        function clearAlert() {
            alertDiv.style.display = 'none';
        }

        function shakeForm(formId) {
            const form = document.getElementById(formId);
            form.classList.add('shake');
            setTimeout(() => {
                form.classList.remove('shake');
            }, 500);
        }
